<?php
// ===========================================
// データ更新処理（update.php）
// ===========================================
// このファイルは detail.php のフォームから
// POSTで送信されたデータでDBを更新します。
// ===========================================


// -----------------------------------------
// 1. POSTデータを取得
// -----------------------------------------
// フォームの name 属性の値をキーにして取得
// id は hidden フィールドから取得
// -----------------------------------------
$name   = $_POST["name"];
$email  = $_POST["email"];
$naiyou = $_POST["naiyou"];
$id     = $_POST["id"];  // 更新対象のID（hidden）


// -----------------------------------------
// 2. 共通関数ファイルを読み込み、DB接続
// -----------------------------------------
include("funcs.php");
$pdo = db_conn();


// -----------------------------------------
// 3. UPDATE文を作成・実行
// -----------------------------------------
// SET カラム名=値 で更新する値を指定
// WHERE id=:id で更新対象を限定（重要！）
// ※ WHERE を忘れると全データが更新される！
// -----------------------------------------
$sql = "UPDATE gs_an_table 
        SET name = :name, email = :email, naiyou = :naiyou 
        WHERE id = :id";

$stmt = $pdo->prepare($sql);

// プレースホルダーに値をバインド
$stmt->bindValue(':name',   $name,   PDO::PARAM_STR);
$stmt->bindValue(':email',  $email,  PDO::PARAM_STR);
$stmt->bindValue(':naiyou', $naiyou, PDO::PARAM_STR);
$stmt->bindValue(':id',     $id,     PDO::PARAM_INT);

// SQLを実行
$status = $stmt->execute();


// -----------------------------------------
// 4. 実行結果の判定とリダイレクト
// -----------------------------------------
// 成功時: select.php にリダイレクト
// 失敗時: エラーメッセージを表示
// -----------------------------------------
if ($status == false) {
    // SQLエラーの場合
    sql_error($stmt);
} else {
    // 成功時は一覧画面に戻る
    redirect("select.php");
}

