<?php
// ===========================================
// 文字列の結合
// ===========================================

$name = "どんぶラッコ";
$age = 29;

// -----------------------------------------
// 1. ドット演算子で結合
// -----------------------------------------
echo "<h2>1. ドット演算子</h2>";
echo "私の名前は" . $name . "です。<br>";

// -----------------------------------------
// 2. ダブルクォーテーションで変数展開
//    変数がそのまま展開される
// -----------------------------------------
echo "<h2>2. ダブルクォーテーション</h2>";
echo "私の名前は $name です。<br>";
echo "私は{$age}歳です。<br>";

// -----------------------------------------
// 3. シングルクォーテーション
//    変数は展開されない（文字列のまま表示）
// -----------------------------------------
echo "<h2>3. シングルクォーテーション</h2>";
echo '私の名前は $name です。<br>';

// -----------------------------------------
// 4. 結合演算子（.=）
//    JavaScriptの += に相当
// -----------------------------------------
echo "<h2>4. 結合演算子</h2>";
$message = "私の名前は";
$message .= $name;
$message .= "です。";
echo $message;
?>
