<?php
// ===========================================
// 便利な組み込み関数
// ===========================================

// -----------------------------------------
// 1. ランダム数生成 - rand()
// -----------------------------------------
echo "<h2>1. rand()</h2>";

$randomNumber = rand(1, 10);  // 1〜10のランダムな整数
echo "ランダム数: " . $randomNumber . "<br>";

// サイコロの例
echo "サイコロ: " . rand(1, 6) . "<br>";

// -----------------------------------------
// 2. 文字列分割 - explode()
// -----------------------------------------
echo "<h2>2. explode()</h2>";

$text = "りんご,バナナ,オレンジ";
$fruits = explode(",", $text);  // カンマで分割して配列にする

echo "<pre>";
var_dump($fruits);
echo "</pre>";

// -----------------------------------------
// 3. 日付・時刻 - date()
// -----------------------------------------
echo "<h2>3. date()</h2>";

echo date("Y年m月d日") . "<br>";      // 2025年12月09日
echo date("H:i:s") . "<br>";          // 14:30:00
echo date("Y-m-d H:i:s") . "<br>";    // 2025-12-09 14:30:00

// よく使うフォーマット
// Y: 年（4桁）
// m: 月（2桁）
// d: 日（2桁）
// H: 時（24時間）
// i: 分
// s: 秒

// -----------------------------------------
// 4. 当たり外れアプリの例
// -----------------------------------------
echo "<h2>4. おみくじアプリ</h2>";

$results = ["大吉", "中吉", "小吉", "凶"];
$randomIndex = rand(0, count($results) - 1);

echo "今日の運勢: " . $results[$randomIndex];
?>
