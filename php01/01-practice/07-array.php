<?php
// ===========================================
// 配列の作成と操作
// ===========================================

// -----------------------------------------
// 1. 基本的な配列
// -----------------------------------------
echo "<h2>1. 基本的な配列</h2>";
$fruits = ["りんご", "バナナ", "オレンジ"];

echo $fruits[0] . "<br>";  // りんご
echo $fruits[1] . "<br>";  // バナナ
echo $fruits[2] . "<br>";  // オレンジ

// -----------------------------------------
// 2. 配列への要素追加
//    $配列名[] = 値; で末尾に追加
// -----------------------------------------
echo "<h2>2. 要素の追加</h2>";
$numbers = [1, 2, 3];
$numbers[] = 4;
$numbers[] = 5;

echo "<pre>";
var_dump($numbers);
echo "</pre>";

// -----------------------------------------
// 3. 連想配列（キーと値のペア）
// -----------------------------------------
echo "<h2>3. 連想配列</h2>";
$person = [
    "name" => "田中太郎",
    "age" => 25,
    "city" => "東京"
];

echo $person["name"] . "<br>";
echo $person["age"] . "<br>";
echo $person["city"] . "<br>";

// -----------------------------------------
// 4. 配列の要素数を取得
// -----------------------------------------
echo "<h2>4. 要素数の取得</h2>";
echo "fruitsの要素数: " . count($fruits) . "<br>";
?>
