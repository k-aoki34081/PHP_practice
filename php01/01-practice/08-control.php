<?php
// ===========================================
// 条件分岐・繰り返し・関数
// ===========================================

// -----------------------------------------
// 1. 条件分岐（if文）
// -----------------------------------------
echo "<h2>1. 条件分岐</h2>";

$age = 25;

if ($age >= 20) {
    echo "成人です<br>";
} else {
    echo "未成年です<br>";
}

// elseif の例
$score = 75;

if ($score >= 80) {
    echo "優秀です！<br>";
} elseif ($score >= 60) {
    echo "合格です！<br>";
} else {
    echo "もう少し頑張りましょう<br>";
}

// -----------------------------------------
// 2. 繰り返し（for文）
// -----------------------------------------
echo "<h2>2. for文</h2>";

for ($i = 1; $i <= 5; $i++) {
    echo $i . " ";
}
echo "<br>";

// -----------------------------------------
// 3. 繰り返し（foreach文）
//    配列の要素を1つずつ取り出す
// -----------------------------------------
echo "<h2>3. foreach文</h2>";

$fruits = ["りんご", "バナナ", "オレンジ"];

foreach ($fruits as $fruit) {
    echo $fruit . "<br>";
}

// キーと値の両方を取得する場合
$person = ["name" => "田中", "age" => 25];

foreach ($person as $key => $value) {
    echo $key . ": " . $value . "<br>";
}

// -----------------------------------------
// 4. 関数の定義と呼び出し
// -----------------------------------------
echo "<h2>4. 関数</h2>";

// 関数を定義
function greet($name) {
    return "こんにちは、" . $name . "さん！";
}

// 関数を呼び出し
echo greet("田中") . "<br>";
echo greet("佐藤") . "<br>";

// デフォルト引数
function sayHello($name = "ゲスト") {
    return "ようこそ、{$name}さん！";
}

echo sayHello() . "<br>";       // デフォルト値を使用
echo sayHello("山田") . "<br>"; // 引数を指定
?>
